x = float(input('Enter ONe number'))
y = float(input('Enter 2nd number'))

choice = input('enter your choice +,-,/,*')

if choice =='+':
    print({x+y})

elif choice =='-':
    print({x-y})

elif choice =='/':
    print({x/y})

elif choice =='*':
    print({x*y})

else:
    print('invalid')
